class Ladder{
	public static void owner(String name){
		System.out.println(name);
	}
	
	public static void length(int length){
		System.out.println(length);
	}
	
	public static void price(double price){
		System.out.println(price);
	}
	
	public static void ownerAddress(String name, String address){
		System.out.println(name);
		System.out.println(address);
	}
	
	public static void shopDetails(String shopName, int shopNum){
		System.out.println(shopName);
		System.out.println(shopNum);
	}
	
	public static void sender(String shopName, String ownerName, String address){
		System.out.println(shopName);
		System.out.println(ownerName);
		System.out.println(address);
	}
	
	public static void receiver(String name, long phNo, String address){
		System.out.println(name);
		System.out.println(phNo);
		System.out.println(address);
	}
	
	
	
}