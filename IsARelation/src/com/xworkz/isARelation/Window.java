package com.xworkz.isARelation;

public class Window {
    public Window() {
        System.out.println("no-arg const of Window");
    }

    public void open() {
        System.out.println("Opening the window");
    }

    public void close() {
        System.out.println("Closing the window");
    }

    public void allowVentilation() {
        System.out.println("Allowing ventilation through the window");
    }

    public void provideView() {
        System.out.println("Providing outside view through the window");
    }

    public void protectFromWeather() {
        System.out.println("Protecting from rain and wind");
    }
}
